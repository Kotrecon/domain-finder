# 💡 ПРИНЦИПЫ РАБОТЫ
# 🏗 АРХИТЕКТУРА: Централизованная конфигурация (Infrastructure Layer)
# - Ответственность: хранение настроек, зависимых от окружения, без бизнес-логики
# - Паттерны: Environment Variables + Fallback Lists + Singleton Constants
# - Граница: не содержит логики провайдеров/кэша — только данные для их инициализации
#
# 1. РАЗДЕЛЕНИЕ ПО ПРОВАЙДЕРАМ
#    • OpenRouter: облачный API с ключом, бесплатные модели, обязательные заголовки
#    • Ollama: локальный сервер без ключа, гибкий адрес через .env
#    • Бизнес-смысл: добавление нового провайдера — новый блок конфигов, не правка существующих
#
# 2. FALLBACK-СТРАТЕГИЯ
#    • Списки моделей упорядочены: первая доступная используется, остальные — резерв
#    • Бизнес-правило: не останавливать обработку из-за недоступности одной модели
#    • Зачем: повышение доступности сервиса при временных проблемах у провайдеров
#
# 3. ГИБКОСТЬ ОКРУЖЕНИЯ (12-Factor App)
#    • os.getenv() с дефолтами: приложение работает «из коробки», настраивается через .env
#    • Чувствительные данные не в коде — безопасность и соответствие лучшим практикам
#    • Одна кодовая база для dev/stage/prod, различия только в .env
#
# 4. ЦЕНТРАЛИЗАЦИЯ КОНСТАНТ
#    • TTL, URL, заголовки — в одном месте: изменение в одном файле, а не поиск по проекту
#    • Бизнес-выгода: снижение риска ошибок при обновлении настроек
#
# 🔒 БЕЗОПАСНОСТЬ И НАДЕЖНОСТЬ
#    • OPENROUTER_API_KEY = None если не задан: провайдер деактивируется, не крашит приложение
#    • DEFAULT_HEADERS: фиксированные значения — защита от опечаток в каждом запросе
#    • os.getenv без дефолта для ключей: явный сигнал «ключ не задан»
#
# 🚀 ПОРЯДОК ИНТЕГРАЦИИ
#    [load_dotenv()] -> [импорт констант в модули] -> [проверка ключа на None]
#    -> [использование TTL в кэше] -> [применение LOG_LEVEL в logger]
#
# 📊 МЕТРИКИ ДЛЯ БИЗНЕСА
#    • Provider fallback rate: как часто используется не первая модель из списка
