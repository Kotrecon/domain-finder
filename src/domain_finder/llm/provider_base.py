from abc import ABC, abstractmethod
from typing import Optional

# Базовый абстрактный интерфейс для всех LLM-провайдеров (OpenRouter, Ollama, etc.)
# Бизнес-правило: единый контракт — замена провайдера не требует правки вызывающего кода
class LLMProvider(ABC):
    # Инициализация: имя провайдера + флаги состояния (подключен/ключ валиден)
    # Бизнес-смысл: is_connected/is_key_valid разделяют сетевые ошибки от ошибок авторизации
    def __init__(self, name: str):
        self.name = name
        self.is_connected = False
        self.is_key_valid = False
    
    # Загрузка конфигурации: чтение ключей/URL из конфига, валидация формата
    # Бизнес-правило: возврат False при ошибке — ранний фейл, чтобы не падать в рантайме
    @abstractmethod
    def load_config(self) -> bool:
        pass
    
    # Проверка соединения: тестовый запрос без генерации (ping/health-check)
    # Бизнес-смысл: разделение "сеть работает" и "ключ валиден" — упрощает диагностику
    @abstractmethod
    def validate_connection(self) -> bool:
        pass
    
    # Проверка доступности модели: запрос к конкретному model_name
    # Бизнес-правило: возврат False при недоступности — вызывающий код может переключиться на другую модель
    @abstractmethod
    def check_model(self, model_name: str) -> bool:
        pass
    
    # Генерация ответа: полноценный запрос с промптом, возврат сырого текста
    # Бизнес-смысл: Optional[str] — None или исключение при ошибке, вызывающий код решает стратегию обработки
    @abstractmethod
    def generate(self, prompt: str, model: str, max_tokens: int = 1024) -> Optional[str]:
        pass
    
    # Готовность к работе: конъюнкция is_connected AND is_key_valid
    # Бизнес-правило: единая точка проверки перед генерацией — защита от вызова неинициализированного провайдера
    def is_ready(self) -> bool:
        return self.is_connected and self.is_key_valid


# 💡 ПРИНЦИПЫ РАБОТЫ
# 🏗 АРХИТЕКТУРА: Абстрактный интерфейс LLM-провайдера (Provider Contract)
# - Ответственность: определение единого контракта для всех реализаций провайдеров
# - Паттерны: Abstract Base Class + Dependency Inversion + Strategy
#
# 1. ЕДИНЫЙ КОНТРАКТ
#    • load_config/validate_connection/check_model/generate — фиксированный набор методов
#    • Возврат bool/Optional[str] — предсказуемые типы для вызывающего кода
#    • is_ready(): готовность = (ключ валиден) AND (соединение активно) — единая логика для всех
#
# 2. РАЗДЕЛЕНИЕ ОТВЕТСТВЕННОСТИ
#    • is_key_valid: ошибка авторизации/конфига — требует правки настроек
#    • is_connected: сетевая ошибка/сервер недоступен — может быть временной
#    • Бизнес-смысл: разные реакции на разные типы ошибок (не перезапускать приложение из-за таймаута)
#
# 3. ГИБКОСТЬ И МАСШТАБИРУЕМОСТЬ
#    • Новый провайдер: наследовать LLMProvider, реализовать 4 абстрактных метода
#    • Вызывающий код работает через интерфейс — не знает, OpenRouter это или Ollama
#    • Стратегия fallback: при ошибке generate() можно переключиться на другой провайдер
#
# 🔒 БЕЗОПАСНОСТЬ И НАДЕЖНОСТЬ
#    • Абстрактные методы: гарантия, что реализация не забудет критичную логику
#    • is_ready() перед генерацией: защита от вызова неинициализированного провайдера
#    • Optional[str] в generate(): явный сигнал, что ответ может отсутствовать — обработка у вызывающего кода
#
# 🚀 ПОРЯДОК ВЫПОЛНЕНИЯ
#    [Создание провайдера: OllamaProvider()] -> [load_config(): загрузка ключей/URL]
#    -> [validate_connection(): проверка сети] -> [check_model("llama3.2"): проверка модели]
#    -> [is_ready()? да] -> [generate(prompt): запрос к модели → возврат текста]
#    -> [При ошибке: вызывающий код ловит RuntimeError → retry / fallback / abort]